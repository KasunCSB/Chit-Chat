/**
 * Generate a 5-word human-readable passphrase
 */
export function generatePassphrase(wordCount = 5) {
  const words = [];
  for (let i = 0; i < wordCount; i++) {
    const idx = Math.floor(Math.random() * WORDLIST.length);
    words.push(WORDLIST[idx]);
  }
  return words.join('-');
}

/**
 * Normalize and validate a passphrase
 */
export function normalizePassphrase(input) {
  if (!input || typeof input !== 'string') {
    return null;
  }
  
  const normalized = input.trim().toLowerCase();
  
  // Length validation (prevent excessively long inputs)
  // Minimum 1 char to allow short codes, max 200 for passphrases
  if (normalized.length < 1 || normalized.length > 200) {
    return null;
  }
  
  // Only allow lowercase letters, numbers, and hyphens (more permissive for short codes)
  if (!/^[a-z0-9-]+$/.test(normalized)) {
    return null;
  }
  
  return normalized;
}
